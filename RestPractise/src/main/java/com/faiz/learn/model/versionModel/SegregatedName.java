package com.faiz.learn.model.versionModel;

public class SegregatedName {

	private Name name;

	public SegregatedName() {
		super();
	}

	public SegregatedName(Name name) {
		super();
		this.name = name;
	}

	public Name getName() {
		return name;
	}

	public void setName(Name name) {
		this.name = name;
	}

}
